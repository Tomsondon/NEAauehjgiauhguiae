def encodeMaze(maze):
    encoding = []
    for i in range(len(maze)):
        for j in range(len(maze[i]) // 3):
            periodSum = maze[i][(3*j)] * 16 + maze[i][(3*j) + 1] * 4 + maze[i][(3*j) + 2]
            encoding.append(encodingCharacters[periodSum])
    return "".join(encoding)

def decodeMaze(encoding):
    board = []
    row = []
    counter = 0
    for char in list(encoding):
        counter += 1
        integerPeriodSum = encodingCharacters.index(char)
        row.append(integerPeriodSum // 16)
        integerPeriodSum -= (integerPeriodSum // 16) * 16
        row.append(integerPeriodSum // 4)
        integerPeriodSum -= (integerPeriodSum // 4) * 4
        row.append(integerPeriodSum)
        if counter % 10 == 0:
            board.append(row)
            row = []
            counter = 0

    return board
    
