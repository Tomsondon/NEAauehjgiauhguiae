def mazeMapping(maze):  # breadth first search algorithm to create weighted graph adjacency list of maze
    junctions = []
    queue = []
    for i in range(len(maze)):
        for j in range(len(maze[i])):
            if maze[i][j] != 3:
                if maze[i + 1][j] != 3 or maze[i - 1][j] != 3:
                    if maze[i][j + 1] != 3 or maze[i][j - 1] != 3:
                        junctions.append([i, j])

    return junctions


